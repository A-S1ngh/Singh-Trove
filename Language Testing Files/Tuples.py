players = ("Kahwi", "Lebron", "Giannis") #This is a tuple. Order Matters, but you cannot make changes to it.
#Tuples have only 2 methods, Index and Count.
kcount = players.count("Kahwi")
lindex = players.index("Lebron")
print(f"The amount of times that Kahwi shows up is {kcount}")
print(f"Lebron's index in this tuple is {lindex}")
